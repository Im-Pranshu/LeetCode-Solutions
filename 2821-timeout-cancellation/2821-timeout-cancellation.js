/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
var cancellable = function(fn, args, t) {
    
    // clearTimout will be called if t < cancelTimeMs(in this time cancel fn will be called)
    const cancelFn = () => {
        clearTimeout(timer)
    }


    const timer = setTimeout(() => {
            // call the fn with the following args
            fn(...args)
    }, t)

    // return the cancelFn - if t is less then clearTimeout will be called and fn will not be called.
    return cancelFn
};

